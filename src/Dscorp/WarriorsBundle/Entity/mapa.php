<?php

namespace Dscorp\WarriorsBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * mapa
 */
class mapa
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $urlimagenMapa;

    /**
     * @var string
     */
    private $descripcionMapa;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set urlimagenMapa
     *
     * @param string $urlimagenMapa
     * @return mapa
     */
    public function setUrlimagenMapa($urlimagenMapa)
    {
        $this->urlimagenMapa = $urlimagenMapa;

        return $this;
    }

    /**
     * Get urlimagenMapa
     *
     * @return string 
     */
    public function getUrlimagenMapa()
    {
        return $this->urlimagenMapa;
    }

    /**
     * Set descripcionMapa
     *
     * @param string $descripcionMapa
     * @return mapa
     */
    public function setDescripcionMapa($descripcionMapa)
    {
        $this->descripcionMapa = $descripcionMapa;

        return $this;
    }

    /**
     * Get descripcionMapa
     *
     * @return string 
     */
    public function getDescripcionMapa()
    {
        return $this->descripcionMapa;
    }
}
